package ru.job4j.condition;

public class Liken {
    public static void main(String[] args) {
        int first = 10;
        int second = 9;
        boolean resultat = first > second;
        System.out.println(resultat);
        resultat = first < second;
        System.out.println(resultat);
        resultat = first == second;
        System.out.println(resultat);
    }
}
